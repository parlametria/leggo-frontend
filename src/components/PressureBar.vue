<template>
    <div class="progress">
        <div class="progress-bar progress-bar-info progress-bar-striped"
        role="progressbar"
        :style="retornaEstilo()">
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'

export default {
  name: 'PressureBar',
  props: ['energia'],
  methods: {
    pegaPorcentagem () {
      return (this.energia * 100) / this.maxPressao
    },
    retornaEstilo () {
      return `width:${this.pegaPorcentagem()}%`
    }
  },
  computed: {
    ...mapGetters(['maxPressao'])
  }
}
</script>

